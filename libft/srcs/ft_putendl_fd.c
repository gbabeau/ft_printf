#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	tail;

	if (s)
	{
		tail = 0;
		while (s[tail] != '\0')
			tail++;
		write(fd, s, tail);
		write(fd, "\n", 1);
	}
}
